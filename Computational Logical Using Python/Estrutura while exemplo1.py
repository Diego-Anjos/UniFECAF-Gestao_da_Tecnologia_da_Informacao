# Exemplo de estrutura de repetição.
# Enquanto a condição contador <= 100 for verdadeira, o bloco de código dentro do loop será executado. 
# Ou seja, o loop continuará a ser executado enquanto o valor de contador for menor ou igual a 100.

contador = 0
while contador <= 100:
    print (contador)
    contador = contador + 10
print ('FIM DO PROGRAMA')
